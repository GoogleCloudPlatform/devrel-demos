/**
 * @file use-mobile.tsx
 * @description Hook to detect if the screen size is below the mobile breakpoint.
 * Why it matters: Used for responsive UI adjustments like sidebar behavior.
 */

import * as React from "react"

// Breakpoint for determining mobile screen size
const MOBILE_BREAKPOINT = 768

// Custom hook to detect if the current screen size is below the mobile breakpoint.
export function useIsMobile() {
  const [isMobile, setIsMobile] = React.useState<boolean | undefined>(undefined)

  React.useEffect(() => {
    const mql = window.matchMedia(`(max-width: ${MOBILE_BREAKPOINT - 1}px)`)
    const onChange = () => {
      setIsMobile(window.innerWidth < MOBILE_BREAKPOINT)
    }
    mql.addEventListener("change", onChange)
    setIsMobile(window.innerWidth < MOBILE_BREAKPOINT)
    return () => mql.removeEventListener("change", onChange)
  }, [])

  return !!isMobile
}
